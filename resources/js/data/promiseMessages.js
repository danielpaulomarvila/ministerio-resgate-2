function normalizeCategory(category) {
    return category
        .normalize('NFD')
        .replace(/[\u0300-\u036f]/g, '')
        .toLowerCase()
        .replace(/[^a-z0-9]+/g, '-')
        .replace(/(^-|-$)/g, '');
}

export const promiseMessages = messageGroups.flatMap((group) =>
    group.entries.map((entry, index) => ({
        id: `${normalizeCategory(group.category)}-${String(index + 1).padStart(3, '0')}`,
        category: group.category,
        ...entry,
    })),
);

export const promiseCategories = messageGroups.map((group) => group.category);

export default promiseMessages;
